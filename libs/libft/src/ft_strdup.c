#include <libft.h>

char	*ft_strdup(const char *s)
{
	char	*dups;
	size_t	i;

	dups = (char *) malloc((ft_strlen(s) + 1) * sizeof(char));
	if (!dups)
		return (NULL);
	i = 0;
	while (s[i])
	{
		dups[i] = s[i];
		i++;
	}
	dups[i] = '\0';
	return (dups);
}
